          function login(Request $req){
        $data = $req->input();
    
        $user = User::where('name', $data['username'])->firstOrFail();
        $isAdmin = $user->is_admin;
            
        $req->session()->put('user', [
            'name' => $data['username'],
            'is_admin' => $isAdmin
        ]);
            
        return view('miestai', [
            'user' => session('user')
        ]);
